# Ask for user's name
name = input("Enter your name: ") # ruhul

# Print the name
print(f"Hello, ", name)  # Hello,  ruhul

# Remove whitespace from the name
name = name.strip()
print(f"Hello, ", name)  # Hello,  ruhul

# Capitalize the name
name = name.capitalize()
print(f"Apply capitalize() to name, ", name)  #  Ruhul amin

# Capitalize the name
name = name.title()
print(f"Apply title() to name, ", name)  #  Ruhul Amin

#
name = name.strip().upper()
print(f"Apply strip() and upper() to name, ", name)  #  RUHUL AMIN

